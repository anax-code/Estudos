    public void morrendo() {

        switch (jogo.getVidas()) {
            case 6:
                Image morrendo7 = new ImageIcon(getClass().getResource("forca.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo7));

                break;
            case 5:
                Image morrendo6 = new ImageIcon(getClass().getResource("forca2.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo6));

                break;
            case 4:

                Image morrendo5 = new ImageIcon(getClass().getResource("forca3.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo5));
                break;
            case 3:

                Image morrendo4 = new ImageIcon(getClass().getResource("forca4.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo4));
                break;
            case 2:
                Image morrendo3 = new ImageIcon(getClass().getResource("forca5.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo3));
                break;
            case 1:
                Image morrendo2 = new ImageIcon(getClass().getResource("forca6.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo2));
                break;
            case 0:
                Image morrendo = new ImageIcon(getClass().getResource("forca7.jpg")).getImage();
                jLabel1.setIcon(new ImageIcon(morrendo));
                break;

        }
    }

