    private void jBOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBOkActionPerformed
        char letras = 0;
        try {
            if (jTLetraDigitada.getText().isEmpty()) {
            }

            if (jogo.getVidas() < 1) {
                jogo.colocavida(0);
            } else if (jogo.getVidas() > 8) {
                jogo.colocavida(7);

            }
            letras = jTLetraDigitada.getText().charAt(0);
            jogo.setLetra(letras);
        } catch (StringIndexOutOfBoundsException erro) {
            if (jogo.getVidas() - 1 > 0) {
                JOptionPane.showMessageDialog(this, "Não deixe vazio" + "\n" + "Coloque uma letra!", "Erro", 0);
            }

        }

        jTFPalavra.setText(Arrays.toString(jogo.acertos(this.varii)));
        jTLetraDigitada.setText("");
        jTLetraDigitada.requestFocusInWindow();

        jogo.perdendo();

        morrendo();

        if (jogo.getVidas() == 3) {
            JOptionPane.showMessageDialog(this, jogo.getVidas() + "   " + "vidas restantes melhor usar a dica!");

        }
        jTFErrosRestantes.setText(String.valueOf(jogo.getVidas()));

        if (jogo.compara() == true) {
            JOptionPane.showMessageDialog(null, "Você ganhou");
            jBNovoJogo.setEnabled(true);
            jBAjuda.setEnabled(false);
            jBOk.setEnabled(false);
            jTLetraDigitada.setEnabled(false);
            jTFPalavra.setText("");
            jTFErrosRestantes.setText(String.valueOf(jogo.getVidas()));
            jogo = new JogoDaForca();

        }
        if (jogo.getVidas() == 0) {
            JOptionPane.showMessageDialog(this, "Você perdeu, " + "\n" + "a palavra era:" + "  " + jogo.getPalavraSorteada(), "Jogo da Forca", 1);

            System.exit(0);

        }


    }//GEN-LAST:event_jBOkActionPerformed

